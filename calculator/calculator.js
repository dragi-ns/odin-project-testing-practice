const calculator = {
  add(leftOperand, rightOperand) {
    return leftOperand + rightOperand;
  },
  subtract(leftOperand, rightOperand) {
    return leftOperand - rightOperand;
  },
  divide(leftOperand, rightOperand) {
    return leftOperand / rightOperand;
  },
  multiply(leftOperand, rightOperand) {
    return leftOperand * rightOperand;
  },
};

export default calculator;
